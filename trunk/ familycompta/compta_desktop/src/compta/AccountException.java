package compta;

public class AccountException extends Exception {

	public AccountException() {
		super();
	}

	public AccountException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AccountException(String arg0) {
		super(arg0);
	}

	public AccountException(Throwable arg0) {
		super(arg0);
	}

}
